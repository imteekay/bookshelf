const booksPerYear = [
  [2020, twoThousandTwentybooks],
  [2019, twoThousandNineteenbooks],
];

const App = {
  data() {
    return {
      booksPerYear
    }
  },
};

const app = Vue.createApp(App);

app.component('book-check', {
  props: {
    read: {
      type: Boolean,
      required: true,
    },
  },
  template: `
    <span class='book-read' v-if="read">
      <i class="fa fa-check"></i>
    </span>
  `,
});

app.component('book-info', {
  props: {
    title: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    url: {
      type: String,
      required: true,
    }
  },
  template: `
    <b>
      <a
        :href="url"
        target='_blank'
      >
        {{ title }}
      </a>
    </b>
    by {{ author }}
  `,
});

app.component('book-article', {
  props: {
    hasArticle: {
      type: Boolean,
      required: true,
    },
    articleUrl: {
      type: String,
      required: true,
    }
  },
  template: `
    <span class="medium-post" v-if="hasArticle">
      <a :href="articleUrl">
        <i class='fa fa-medium'></i>
      </a>
    </span>
  `,
});

app.component('book', {
  props: {
    read: {
      type: Boolean,
    },
    title: {
      type: String,
    },
    author: {
      type: String,
    },
    url: {
      type: String,
    },
    hasArticle: {
      type: Boolean,
    },
    articleUrl: {
      type: String,
    },
  },
  template: `
    <li>
      <book-check v-bind:read="read"></book-check>
      <book-info
        v-bind:title="title"
        v-bind:author="author"
        v-bind:url="url"
      ></book-info>
      <book-article
        v-bind:has-article="hasArticle"  
        v-bind:article-url="articleUrl"  
      ></book-article>
    </li>
  `,
});

app.component('books-per-year', {
  props: {
    year: {
      type: Number,
    },
    books: {
      type: Array,
    },
  },
  template: `
    <hr class="divider">
    <h2>{{ year }}</h2>
    <ul>
      <book
        v-for="book in books"
        v-bind:read="book.read"
        v-bind:title="book.title"
        v-bind:author="book.author"
        v-bind:url="book.url"
        v-bind:has-article="book.hasArticle"
        v-bind:article-url="book.articleUrl"
        v-bind:key="book.url"
      ></book>
    </ul>
  `,
});

app.mount('#app');
